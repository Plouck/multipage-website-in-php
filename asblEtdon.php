                <p>Télé-Accueil Bruxelles existe depuis plus de cinquante ans !</p>
                <ul>
                  <li><strong>1953 :</strong> L’idée de proposer une écoute par téléphone vient de Grande-Bretagne. Suite au suicide d’un adolescent de 14 ans, le révérend Chad Varah publia une annonce dans le Times du 2 novembre 1953 : « Avant de vous suicider, appelez-moi à M.A.N. 90.00 ». Il inventa ainsi le premier service d’écoute téléphonique, un service pour venir en aide à tous ceux qui étaient tentés par le suicide, aux désespérés, aux isolés et à tous ceux qui ne se sentaient écoutés de personne. Rapidement, d'autres postes d’écoute vont s'ouvrir dans le monde.</li>
                  <li><strong>1959 :</strong> création du premier poste de Télé-Accueil à Bruxelles.</li>
                  <li><strong>1967 :</strong> création d’une fédération internationale IFOTES (Fédération internationale des Services de Secours par téléphone).</p>
                  <li><strong>1973 :</strong> Télé-Accueil devient une asbl.</li>
                  <li><strong>1976 :</strong> Télé-Accueil est reconnu comme service d’utilité publique.</li>
                  <li><strong>1977 :</strong> création de la Fédération belge des Télé-Accueil francophones (6 postes).</li>
                  <li><strong>1990 :</strong> création du CEFEC (Centre de Formation à l’écoute).</li>
                  <li><strong>1992 :</strong> attribution d’un numéro d’urgence gratuit : le 107.</li>
                  <li><strong>1998 :</strong> création de l’Observatoire social.</li>
                  <li><strong>2005 :</strong> Télé-Accueil diversifie son offre d’écoute et devient la première association en Belgique francophone à offrir une écoute par « chat » sur internet : le Chat-Accueil.</li>
